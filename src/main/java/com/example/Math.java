public class Math {
    public int multiply(int a,int b){
        return a*b;
    }

    public int add(int a,int b){
        return a+b;
    }

    public int sub(int a,int b){
        return a-b;
    }
    public int sub_ox(int a,int b){
        return a*2-b;
    }

    public int add_oo(int a,int b){
        return a*2-b;
    }
    public int sub_fff(int a,int b){
        return a*2-b*2;
    }

    public int add_fdf(int a,int b){
        return a*2-b*3;
    }
    public int add_cc(int a,int b){
        return b-a;
    }

    public int add_cc_2(int a,int b){
        return b-a;
    }
    public int add_cc_3(int a,int b){
        return b-a;
    }
}

